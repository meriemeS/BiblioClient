    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       JFileChooser chooser = new JFileChooser();
       chooser.setFileFilter(new FileFilter() {

            @Override
            public String getDescription() {
                return "*.pdf";
            }

            @Override
            public boolean accept(File f) {
                return f.isFile() && f.getName().toLowerCase().endsWith(".pdf");
            }
        });
        int r = chooser.showOpenDialog(jButton1);
                if (r == JFileChooser.APPROVE_OPTION) {
                    path = chooser.getSelectedFile().getAbsolutePath();
                    jLabel8.setText(path);
                }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nom_aut = jTextField2.getText();
        String lib_cat = jTextField4.getText();
        String Titre = jTextField1.getText();
        SecureRandom rand = new SecureRandom();
        int  n = rand.nextInt(50) + 1;
        try {
            Categorie cat = categorieInterface.findByLib(lib_cat);
            Auteur aut = auteurInterface.findByNom(nom_aut);
            if (cat != null && aut != null){
                Livre l = new Livre();
                l.setId(n);
                l.setTitre(Titre);
                l.setId_aut(aut.getId());
                l.setId_cat(cat.getId());
                l.setPdf("C:\\Users\\pc\\Documents\\NetBeansProjects\\Biblio\\src\\pdf\\"+Titre+".pdf");
                l.setNom_aut(nom_aut);
                l.setLib_cat(lib_cat);
                l.setImages(null);
                l.setPrenom_aut(aut.getPrenom());
                livreService.save(l);
            }
            
        } catch (Exception ex) {
            Logger.getLogger(AjoutLivre.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
        
    }//GEN-LAST:event_jButton2ActionPerformed

